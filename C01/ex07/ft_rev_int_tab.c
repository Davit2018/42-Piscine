void	ft_rev_int_tab(int *tab, int size)
{
	int	x;
	int	y;
	int	z;

	x = 0;
	y = size - 1;
	while (x < (size / 2))
	{
		z = tab[x];
		tab[x] = tab[y];
		tab[y] = z;
		x++;
		y--;
	}
}
