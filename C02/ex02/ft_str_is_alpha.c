int	ft_str_is_alpha(char *str)
{
	int	a;
	int	b;

	a = 0;
	b = 1;
	while (str[a])
	{
		if ((str[a] >= 'A' && str[a] <= 'Z') || \
				(str[a] >= 'a' && str[a] <= 'z'))
			b = 1;
		else
			return (0);
		a++;
	}
	return (b);
}
